package com.security.service.video;

public class VideoSize {
    private int width;
    private int height;
    
    public VideoSize(int width, int height) {
        this.width = width;
        this.height = height;
    }
    
    public int getWidth() {
        return width;
    }
    
    public int getHeight() {
        return height;
    }
} 